// Menghitung Luas Persegi Panjang
let panjang = 10;
let lebar = 20;

let luas = panjang * lebar;

console.log('Luas Persegi Panjang yang mempunyai panjang ' + panjang + ' cm' + ' dan lebar ' + lebar + ' cm' + ' Menggunakan Rumus p * l' + ' adalah ' + luas + ' cm');


// Menghitung Keliling Persegi Panjang
let p = 10;
let l = 20;

let keliling = 2 * (p + l);
console.log('Keliling Persegi Panjang yang mempunyai panjang ' + p + ' cm' + ' dan lebar ' + l + ' cm' + ' Menggunakan Rumus 2 * (p + l)' + ' adalah ' + keliling + ' cm');